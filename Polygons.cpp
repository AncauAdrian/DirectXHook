#include "Polygons.h"

Poly::Poly(std::string name)
{
	this->name = name;
}

Poly::Poly(const Poly& p)
{
	this->vertx = p.getX();
	this->verty = p.getY();
	this->name = p.getName();
}


void Poly::add(float x, float y)
{
	{
		this->vertx.push_back(x);
		this->verty.push_back(y);
	}
}


std::vector<float> const& Poly::getX() const
{
	return this->vertx;
}

std::vector<float> const& Poly::getY() const
{
	return this->verty;
}

std::string const& Poly::getName() const
{
	return this->name;
}

bool Poly::containsPoint(float testx, float testy)
{
	int nvert = this->vertx.size();
	int i, j, c = 0;
	for (i = 0, j = nvert - 1; i < nvert; j = i++) {
		if (((verty[i] > testy) != (verty[j] > testy)) &&
			(testx < (vertx[j] - vertx[i]) * (testy - verty[i]) / (verty[j] - verty[i]) + vertx[i]))
			c = !c;
	}
	return c;
}

